public class IntegerConstants
{
    public static void main(String [] args)
    {
        int a = -37, b = 89;


        System.out.println("         Decimal         Octal    Hexadecimal"); 
        System.out.println("--------------------------------------------------");
        System.out.printf("a = %12d  %12o  %8x  %8X\n", a, a, a, a);
        System.out.printf("b = %12d  %12o  %8x  %8X\n", b, b, b, b);
    }
}
